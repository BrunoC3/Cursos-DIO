#Inteiro para float

preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)

#Float para inteiro

preco = 10.30
print(preco)

preco = int(preco)
print(preco)

#Conversão por divisão

preco = 10
print(preco)

print(preco / 2) # Retorna um float

print(preco // 2) # Retorna um inteiro

#Convertendo Númerico para String

preco = 10.50
